export type TranslationKey = keyof (typeof messages)["en-US"];
export type Translator = (key: TranslationKey, values?: TranslationValues) => string;

interface I18nContextValue {
  localePreference: LocalePreference;
  locale: ReturnType<typeof detectSystemLocale>;
  t: Translator;
}

const I18nContext = createContext<I18nContextValue | null>(null);

function formatMessage(template: string, values?: TranslationValues) {
  if (!values) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_, key: string) => {
    const value = values[key];
    return value == null ? "" : String(value);
  });
}

export function I18nProvider({
  localePreference,
  children,
}: {
  localePreference: LocalePreference;
  children: ReactNode;
}) {
  const locale = useMemo(
    () => resolveLocalePreference(localePreference),
    [localePreference],
  );

  const value = useMemo<I18nContextValue>(() => {
    const t: Translator = (key, values) =>
      formatMessage(messages[locale][key] ?? messages["en-US"][key] ?? key, values);

    return {
      localePreference,
      locale,
      t,
    };
  }, [locale, localePreference]);

  useEffect(() => {
    document.documentElement.lang = locale;
  }, [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);

  if (!context) {
    throw new Error("useI18n must be used within an I18nProvider");
  }

  return context;
}
